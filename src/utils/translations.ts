// Função para traduzir automaticamente qualquer texto em inglês para português
export const translateToPortuguese = (text: string): string => {
  // Se o texto já está em português, retorna como está
  if (text.includes('jogue') || text.includes('causa') || text.includes('descarte') || 
      text.includes('pokémon') || text.includes('defensor') || text.includes('ataque')) {
    return text
  }
  
  // Traduções automáticas básicas
  const autoTranslations: { [key: string]: string } = {
    'Flip a coin': 'Jogue uma moeda',
    'If heads': 'Se sair cara',
    'the Defending Pokémon': 'o Pokémon Defensor',
    'is now Paralyzed': 'agora está Paralisado',
    'is now Confused': 'agora está Confuso',
    'Discard': 'Descarte',
    'Energy cards': 'cartas de Energia',
    'attached to': 'ligadas ao',
    'in order to use this attack': 'para usar este ataque',
    'Does': 'Causa',
    'damage': 'de dano',
    'plus': 'mais',
    'more damage': 'de dano adicional',
    'for each': 'para cada',
    'Water Energy': 'Energia de Água',
    'but not used to pay': 'mas não usada para pagar',
    'this attack\'s Energy cost': 'o custo de Energia deste ataque',
    'You can\'t add more than': 'Você não pode adicionar mais de',
    'damage in this way': 'de dano desta forma',
    'A powerful attack': 'Um poderoso ataque',
    'that uses the energy of the sun': 'que usa a energia do sol',
    'times the number of': 'multiplicado pelo número de',
    'Energy cards attached to the Defending Pokémon': 'cartas de Energia ligadas ao Pokémon Defensor',
    'O Pokémon Defensor não pode usar ataques durante o próximo turno do seu oponente': 'O Pokémon Defensor não pode usar ataques durante o próximo turno do seu oponente'
  }
  
  let translatedText = text
  for (const [english, portuguese] of Object.entries(autoTranslations)) {
    translatedText = translatedText.replace(new RegExp(english, 'gi'), portuguese)
  }
  
  return translatedText
}
